# Here we will learn the use of ' if - elif - else ' conditonal statement.

score = int(input("Please enter the marks:  "))

if score >= 90 : 
    print("Grade = A")
elif score >= 80 :
    print("Grade = B")
elif score >= 70 :
    print("Grade = C")
elif score >= 60 :
    print("Grade = D")
else :
    print("Fail.")