# Here we will learn and understand how to create the list in python.

# list of string.
arr_list = ["apple","banana","cherry","apple"]
print(arr_list)

# list of int.
arr_list1 = [45,15,5,32]
print(arr_list1)

# list of mixed data types.
arr_list2 = ["alpha",16,16.22,'M']
print(arr_list2)

# list of nested list.

arr_list3 = [15,[1,2,3],[4,5]]
print(arr_list3)