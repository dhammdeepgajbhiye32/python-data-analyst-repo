# Here we will see the functions for tuples in python.

number = (1,6,8,4,5)

# length function.

print(len(number))

# sorted method.

sorted_num = sorted(number)
sorted_num = tuple(sorted_num)
print(sorted_num)

# sum method.

print(sum(number))

# maximum number.

print(max(number))

# minimum number.

print(min(number))