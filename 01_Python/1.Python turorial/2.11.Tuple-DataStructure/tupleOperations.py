# Here we will see concatenation, repetition and Checking for an item in tuple.

# 1. Concatenation.

tuple1 = (1,2,3)
tuple2 = (4,5,6)
tuple3 = tuple1 + tuple2
print(tuple3)

# 2. Repetition.

tuple = ("Hello",)*3
print(tuple)

# 3. Checking for an item in tuple.

tuples = (10,20,30,40,50)
print(10 in tuples)